    public function update(Request $request, User $user)
    {
        // dd($request);


        $validatedData = $request->validate([
            'name' => 'required',
            'lastname' => 'required',
            'email' => 'required',
            'address' => 'required',
            'register_number_doc' => 'required',
            'cv_img' => '',
            'phone_number' => 'required',
            'profile_img' => '',
        ]);

        // dd($validatedData);

        $user = Auth::user();
        
        $user->update($validatedData);
        // dd($user);

        return redirect()->route('dashboard.');

        
    }

